package gerisoft.apirest.entity;

import javax.persistence.*;

/**
 *
 * @author Gurrea
 */
@Entity
@Table(name = "attentions")//Es desa a la taula attentions de la bbdd
public class Attentions {

    @Column(name = "id")
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false, unique = true)
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
